
public class Colector {
	
	private double valorFinc;
	private int meses;
	private double juros;
	
	public double getValorFinc() {
		return valorFinc;
	}
	public void setValorFinc(double valorFinc) {
		this.valorFinc = valorFinc;
	}
	public int getMeses() {
		return meses;
	}
	public void setMeses(int meses) {
		this.meses = meses;
	}
	public double getJuros() {
		return juros;
	}
	public void setJuros(double juros) {
		this.juros = juros;
	}
	
	

}


